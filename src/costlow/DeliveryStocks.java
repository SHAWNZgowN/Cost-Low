    private void saveToDatabase() {
        String name = NameProfile.getText();
        String driver_id = employeeIDField.getText();
        String date = PresentDate.getText();
        String productName = productNameField.getText();
        String quantity = quantityField.getText();
        String measurement = measurementField.getText();

        try (Connection connection = DriverManager.getConnection(JDBC_URL, DB_USER, DB_PASSWORD)) {
            // SQL query to insert data into the database
            String sql = "INSERT INTO delivered_products (driver_id, driver_name, DATE, product_name, quantity, measurement) VALUES (?, ?, ?, ?, ?, ?)";

            try (PreparedStatement preparedStatement = connection.prepareStatement(sql)) {
                preparedStatement.setString(1, driver_id);
                preparedStatement.setString(2, name);
                preparedStatement.setString(3, date);
                preparedStatement.setString(4, productName);
                preparedStatement.setString(5, quantity);
                preparedStatement.setString(6, measurement);

                // Execute the SQL query
                preparedStatement.executeUpdate();
                JOptionPane.showMessageDialog(
                        this,
                        "PRODCT ADDED.",
                        "Success",
                        JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(
                        this,
                        "PEODUCT IS NOT ADDED.",
                        "ERROR",
                        JOptionPane.INFORMATION_MESSAGE);
        }
        productNameField.setText("");
        quantityField.setText("");
        measurementField.setText("");
    }
    
