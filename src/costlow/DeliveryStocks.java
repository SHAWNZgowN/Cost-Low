    private void fetchUserProfile(String username) {// Connect to the database
        try (Connection connection = DriverManager.getConnection(JDBC_URL,DB_USER, DB_PASSWORD)) {
            // Query to retrieve user information based on username
            String query = "SELECT * FROM employee_account WHERE username = ?";
            try (PreparedStatement preparedStatement = connection.prepareStatement(query)) {
                preparedStatement.setString(1, username);
                ResultSet resultSet = preparedStatement.executeQuery();

                // Display user information in the profile frame
                if (resultSet.next()) {
                    String name = resultSet.getString("name");
                    int id = resultSet.getInt("id");
                    String address = resultSet.getString("address");

                    NameProfile.setText(name);
                    employeeIDField.setText(String.valueOf(id));
                    addressField.setText(address);
                }
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
    
