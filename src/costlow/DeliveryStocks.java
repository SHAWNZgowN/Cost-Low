    private void dataRetrieval(String driver_id) {
    // SQL query with JOIN and WHERE to retrieve data based on EmployeeId
    String sql = "SELECT dp.driver_name, dp.DATE, dp.product_name, dp.quantity, dp.measurement " +
                 "FROM delivered_products dp " +
                 "JOIN employee_account ea ON dp.driver_id = ea.id  " +
                 "WHERE dp.driver_id = ?";

    try (Connection connection = DriverManager.getConnection(JDBC_URL, DB_USER, DB_PASSWORD);
         PreparedStatement preparedStatement = connection.prepareStatement(sql)) {

        // Set the parameter in the PreparedStatement
        preparedStatement.setString(1, driver_id);

        // Execute the query
        try (ResultSet resultSet = preparedStatement.executeQuery()) {
            // Clear existing data in the table model
            tableModel.setRowCount(0);

            // Populate the model with retrieved data
            while (resultSet.next()) {
                String driverName = resultSet.getString("driver_name");
                String date = resultSet.getString("date");
                String productName = resultSet.getString("product_name");
                String quantity = resultSet.getString("quantity");
                String measurement = resultSet.getString("measurement");

                tableModel.addRow(new Object[]{driverName, date, productName, quantity, measurement});
            }

            // Set the model to the JTable
            DeliveredTable.setModel(tableModel);
        }

    } catch (SQLException ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error retrieving data from the database.", "Error", JOptionPane.ERROR_MESSAGE);
    }
}
    
    
